#ACTIVITY 5
# 1. Add a legend and grid to a line or bar graph
# 2. Annotate the highest point in your graph

x2 = ["RED","GREEN","BLUE","YELLOW","ORANGE","PINK"]
y2 = [30,30,50,20,10,40]
colors = ['red', 'green', 'blue', 'yellow', 'orange','pink']

fig, axs = plt.subplots(figsize=(10,4))

axs.bar(x2,y2,color=colors, label = 'Bar Graph')
axs.legend(title='Color Preference', labels=x2)
axs.grid(True)

max_index = np.argmax(y2)
max_value = y2[max_index]

plt.annotate(f'FAVORITE COLOR: ({x2[max_index]}, {max_value})', xy=(x2[max_index], max_value), xytext=(50, -10), textcoords='offset points', arrowprops=dict(arrowstyle='->', connectionstyle='arc3,rad=.1'))

plt.title('Enhanced Bar Graph with Legend and Grid')
plt.xlabel('Color')
plt.tight_layout()
plt.show()

