x2 = ["RED","GREEN","BLUE","YELLOW","ORANGE","PINK"]
y2 = [30,30,50,20,10,40]
colors = ['red', 'green', 'blue', 'yellow', 'orange','pink']
fig, axs = plt.subplots(2,1,figsize=(6,6))
